#Проверка делимости одного числа на другое 
x = float(input("ВВедите делимое число: "))
y = float(input("ВВедите делитель числа: "))

ost = x % y
cel = x // y

if (ost == 0):
	print("Число поделилось нацело. Результат: " + str(cel))
else:
	print("Число поделилось с остатком. Результат нацело: " + str(cel) + "; остаток: " + str(ost))